    governorate: null,
    zipCode: null,
    squareMeters: null,
    proofOfOwnership: null,
    latitude: null,
    longitude: null,
    primaryImage: null,
    mediaFiles: null,
    amenities: null,
    rules: null,
  );

  factory PropertyAddModel.fromEntity(PropertyAddEntity entity) {
    return PropertyAddModel(
      title: entity.title,
      description: entity.description,
      type: entity.type,
      isShared: entity.isShared,
      maxOccupants: entity.maxOccupants,
      bedrooms: entity.bedrooms,
      beds: entity.beds,
      bathrooms: entity.bathrooms,
      price: entity.price,
      rentalUnit: entity.rentalUnit,
      address: entity.address,
      city: entity.city,
      governorate: entity.governorate,
      zipCode: entity.zipCode,
      squareMeters: entity.squareMeters,
      proofOfOwnership: entity.proofOfOwnership,
      latitude: entity.latitude,
      longitude: entity.longitude,
      primaryImage: entity.primaryImage,
      mediaFiles: entity.mediaFiles,
      amenities: entity.amenities,
      rules: entity.rules,
    );
  }
}
