import 'package:MARN/features/property/domain/entities/property_add_entity.dart';

class PropertyAddModel extends PropertyAddEntity {
  PropertyAddModel({
    required super.title,
    required super.description,
    required super.type,
    required super.isShared,
    required super.maxOccupants,
    required super.bedrooms,
    required super.beds,
    required super.bathrooms,
    required super.price,
    required super.rentalUnit,
    required super.address,
    required super.city,
    required super.governorate,
    required super.zipCode,
    required super.squareMeters,
    required super.proofOfOwnership,
    required super.latitude,
    required super.longitude,
    required super.primaryImage,
    super.mediaFiles,
    super.amenities,
    super.rules,
  });

  factory PropertyAddModel.empty() => PropertyAddModel(
    title: null,
    description: null,
    type: null,
    isShared: false,
    maxOccupants: null,
    bedrooms: null,
    beds: null,
    bathrooms: null,
    price: null,
    rentalUnit: null,
    address: null,
